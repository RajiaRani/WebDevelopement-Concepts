// Concatenation is used to merge the two string or array

// Example 1
let primary = ['red', 'green', 'blue'];
let secondary = ['orange', 'yellow', 'white'];
let newArr = primary.concat(secondary);
console.log(newArr);
console.log(secondary.concat(primary));

