// Case Conversion: toUpperCase() and toLowerCase() methods are used to convert the case of strings.

//Example 1
let lowerText = "hello, world!";
console.log(lowerText.toUpperCase()); // "HELLO, WORLD!"

//Example 2
let upperText = "HELLO I AM RAJIA RANI, FROM INDIA. I LOVE CODING. ITS VERY INTERESTING.";
let ans = upperText.toLowerCase();
console.log("After applied lowercase function = ", ans );